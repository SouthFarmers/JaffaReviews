public class SettingsPOJO {

    boolean friedRating;
    boolean myCriticRating;
    boolean newReleases;

    public boolean isFriedRating() {
        return friedRating;
    }

    public void setFriedRating(boolean friedRating) {
        this.friedRating = friedRating;
    }

    public boolean isMyCriticRating() {
        return myCriticRating;
    }

    public void setMyCriticRating(boolean myCriticRating) {
        this.myCriticRating = myCriticRating;
    }

    public boolean isNewReleases() {
        return newReleases;
    }

    public void setNewReleases(boolean newReleases) {
        this.newReleases = newReleases;
    }
}
